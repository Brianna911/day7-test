package com.example.demo;

public class Company {
    private String name;
    private int id;
    public Company() {
        super();
    }
    public Company(String name, int id) {
        super();
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
